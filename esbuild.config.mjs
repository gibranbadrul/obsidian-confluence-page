import esbuild from "esbuild";
import process from "process";
import { builtinModules } from 'node:module';
import { access, copyFile, mkdir } from 'node:fs/promises';
import { join } from 'node:path';

const banner =
`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = (process.argv[2] === "production");
const outDir = "dist";

/** Copies manifest.json and styles.css into dist after the bundle is built. */
const copyPlugin = {
	name: 'copy-plugin-files',
	setup(build) {
		build.onEnd(async () => {
			const cwd = process.cwd();
			await mkdir(outDir, { recursive: true });
			const manifestSrc = join(cwd, 'manifest.json');
			const stylesSrc = join(cwd, 'styles.css');
			if (await pathExists(manifestSrc)) await copyFile(manifestSrc, join(cwd, outDir, 'manifest.json'));
			if (await pathExists(stylesSrc)) await copyFile(stylesSrc, join(cwd, outDir, 'styles.css'));
		});
	},
};

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtinModules],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: join(outDir, "main.js"),
	minify: prod,
	loader: { ".wasm": "binary" },
	plugins: [copyPlugin],
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}

async function pathExists(filePath) {
	try {
		await access(filePath);
		return true;
	} catch {
		return false;
	}
}
